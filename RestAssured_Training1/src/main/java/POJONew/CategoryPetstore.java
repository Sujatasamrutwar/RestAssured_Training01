package POJONew;

public class CategoryPetstore {

	private int id;
	private String name;
	
	public CategoryPetstore() {}

	public CategoryPetstore(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
